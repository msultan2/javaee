package com.ssl.rmas.entities;

import java.time.Instant;

import org.springframework.data.annotation.CreatedDate;
import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.index.Indexed;
import org.springframework.data.mongodb.core.mapping.DBRef;
import org.springframework.data.mongodb.core.mapping.Document;

@Document
public class PasswordChangeToken {
    @Id
    private String token;
    @DBRef
    @Indexed
    private User user;
    @Indexed(expireAfterSeconds=2592000)
    @CreatedDate
    private Instant createdTimestamp;

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public Instant getCreatedTimestamp() {
        return createdTimestamp;
    }

    public void setCreatedTimestamp(Instant createdTimestamp) {
        this.createdTimestamp = createdTimestamp;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((createdTimestamp == null) ? 0 : createdTimestamp.hashCode());
        result = prime * result + ((token == null) ? 0 : token.hashCode());
        result = prime * result + ((user == null) ? 0 : user.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (!(obj instanceof PasswordChangeToken))
            return false;
        PasswordChangeToken other = (PasswordChangeToken) obj;
        if (createdTimestamp == null) {
            if (other.createdTimestamp != null)
                return false;
        } else if (!createdTimestamp.equals(other.createdTimestamp))
            return false;
        if (token == null) {
            if (other.token != null)
                return false;
        } else if (!token.equals(other.token))
            return false;
        if (user == null) {
            if (other.user != null)
                return false;
        } else if (!user.equals(other.user))
            return false;
        return true;
    }

    @Override
    public String toString() {
        return "PasswordResetToken [token=" + token + ", user=" + user + ", createdTimestamp=" + createdTimestamp + "]";
    }

}
